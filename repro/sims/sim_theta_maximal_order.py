import quaternion_order as qo
ok = qo.ok

HERE = Path(__file__).resolve().parent
OUTPUT_DIR = HERE.parent / "output"
OUTPUT_DIR.mkdir(parents=True, exist_ok=True)


def sigma1_eisenstein(NQ_prime: int, k: int) -> int:
    """sigma_1 of the k-th power of a prime ideal of norm q: 1+q+...+q^k."""
    return sum(NQ_prime ** j for j in range(k + 1))


def main():
    I = qo.ring()
    W = 78
    print("=" * W)
    print("SIM 13 (CORRECTED): icosian theta from the MAXIMAL order; C_2 = 1")
    print("=" * W)
    print(f"  ring rank = {len(I.basis)}   trace-form Gram det = {I.gram_det()} "
          f"(expect 625 = 5^4)   #units = {len(I.units)}")
    print()

