    print(f"      {'type':<10}{'varpi':<9}{'N_Q':<6}{'r':<8}{'r/120':<7}{'1+N':<6}match")
    prime_cases = [
        ("split-11", (3, 1)), ("split-19", (4, 1)), ("split-29", (5, 1)),
        ("inert-2",  (2, 0)), ("inert-3",  (3, 0)),
        ("ramif-5",  (2, 1)),
    ]
    prime_rows = []
    prime_ok = True
    for name, varpi in prime_cases:
        NQ = abs(ok.norm(varpi))
        assert ok.is_totally_positive(varpi)
        r = len(I.enumerate_reduced_norm(varpi))
        rt = r // 120
        pred = 1 + NQ
        good = (r % 120 == 0) and (rt == pred)
        prime_ok = prime_ok and good
        prime_rows.append(dict(type=name, varpi=list(varpi), N_Q=NQ, r=r,
                               r_over_120=rt, pred=pred, ok=good))
        print(f"      {name:<10}{str(varpi):<9}{NQ:<6}{r:<8}{rt:<7}{pred:<6}"
              f"{'OK' if good else 'MISMATCH'}")
    print()

    # ---- 2. the prime-2 LOCAL FACTOR: r-tilde((2)^k) = sigma_1((2)^k) ----
    # The inert prime (2) has norm 4; its powers are generated by 2^k.
    # Eisenstein local factor coefficients: sigma_1((2)^k) = (4^{k+1}-1)/3.
